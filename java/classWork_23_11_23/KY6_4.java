public class KY6_4 {
    public static void main(String[] args) {
        System.out.println(Math.abs(-5.8)); // 打印出-5.8的绝对值
        System.out.println(Math.ceil(3.2)); // 打印出3.2向上取整的结果
        System.out.println(Math.floor(3.8)); // 打印出3.8向下取整的结果
        System.out.println(Math.round(3.8)); // 打印出3.8四舍五入的结果
        System.out.println(Math.round(3.2)); // 打印出3.2四舍五入的结果
        System.out.println(Math.min(3, 2)); // 打印出3和2中的最小值
        System.out.println(Math.max(Math.PI, 4)); // 打印出π和4中的最大值
        System.out.println(Math.log(7.0)); // 打印出7.0的自然对数
        System.out.println(Math.pow(7, 2)); // 打印出7的2次方的结果
        System.out.println(Math.exp(0.4)); // 打印出e的0.4次方的结果
        System.out.println("e is:" + Math.E); // 打印出自然常数e的值
        System.out.println("π is:" + Math.PI); // 打印出圆周率π的值
        System.out.println(Math.random()); // 打印出一个[0,1)之间的随机数

    }
}
