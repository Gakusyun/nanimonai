public class exp2_9 {
    public static void main(String[] args) {
        int number1 = 12;
        int number2 = 21;
        System.out.println("12>21:" + (number1 > number2));
        System.out.println("12<21:" + (number1 < number2));
        System.out.println("12==21:" + (number1 == number2));
        System.out.println("12!=21:" + (number1 != number2));
        System.out.println("12>=21:" + (number1 >= number2));
        System.out.println("12<=21:" + (number1 <= number2));
    }
}
