public class exp2_4 {
    public static void main(String[] args) {
        char a1 = 'd';
        char a2 = '@';
        int p1 = 97;
        int p2 = 45123;
        System.out.println("d在unicode编码中的位置是：" + (int) a1);
        System.out.println("@在unicode编码中的位置是：" + (int) a2);
        System.out.println("unicode编码中97是：" + (char) p1);
        System.out.println("unicode编码中45123是：" + (char) p2);
    }
}
